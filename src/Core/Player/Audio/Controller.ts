export const Controller = {PlayerFilter, PlayerRemove, PlayerPause, PlayerReplay, PlayerResume, PlayerSeek, PlayerSkip};

/**
 * @description –ü—Ä–æ–¥–æ–ª–∂–∞–µ—Ç –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –º—É–∑—ã–∫–∏
 * @param message {ClientMessage} –°–æ–æ–±—â–µ–Ω–∏–µ —Å —Å–µ—Ä–≤–µ—Ä–∞
 */
function PlayerResume (message: ClientMessage): void {
    const {client, guild, author} = message;
    const {player, songs}: Queue = client.queue.get(guild.id);
    const {duration, title, color}: Song = songs[0];

    if (player.state.status === 'paused') {
        player.resume();
        return client.Send({text: `‚ñ∂Ô∏è | [${duration.StringTime}] | Resume song | ${title}`, message, type: 'css', color});
    }
    return client.Send({text: `${author}, –¢–µ–∫—É—â–∏–π —Å—Ç–∞—Ç—É—Å –ø–ª–µ–µ—Ä–∞ [\`\`${player.state.status}\`\`\`]`, message, color: 'RED'});
}
